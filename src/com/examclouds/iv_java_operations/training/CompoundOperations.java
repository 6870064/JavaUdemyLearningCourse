package com.examclouds.iv_java_operations.training;

public class CompoundOperations {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c = 3;
        a+=3;
        b*= 2;
        c += a * b;
        int d = 3 + a * b;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
